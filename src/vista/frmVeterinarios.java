    private void tblVeterinarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVeterinarioMouseClicked
        try {
            int fila = tblVeterinario.getSelectedRow();
            txtIdVeteri.setText(tblVeterinario.getValueAt(fila, 0).toString());
            txtNombreVeteri.setText(tblVeterinario.getValueAt(fila, 1).toString());
            txtApellido1Veter.setText(tblVeterinario.getValueAt(fila, 2).toString());
            txtApellido2Veter.setText(tblVeterinario.getValueAt(fila, 3).toString());
            txtCedulaVeter.setText(tblVeterinario.getValueAt(fila, 4).toString());
            txtCodProfVeter.setText(tblVeterinario.getValueAt(fila, 5).toString());
            txtEmailVeter.setText(tblVeterinario.getValueAt(fila, 6).toString());
            txtTelefonoVeter.setText(tblVeterinario.getValueAt(fila, 7).toString());
            txtDireccionVeter.setText(tblVeterinario.getValueAt(fila, 8).toString());
            int activo = Integer.valueOf(tblVeterinario.getValueAt(fila, 9).toString());
            if (activo != 1) {
                cbxActivo.setSelected(false);
            } else {
                cbxActivo.setSelected(true);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al seleccionar el registro" + ex.toString());
        }
    }//GEN-LAST:event_tblVeterinarioMouseClicked


