    private void tblIdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblIdMouseClicked
        try {
            int fila = tblId.getSelectedRow();
            txtId.setText(tblId.getValueAt(fila, 0).toString());
            txtNombre.setText(tblId.getValueAt(fila, 1).toString());
            txtApellido1.setText(tblId.getValueAt(fila, 2).toString());
            txtApellido2.setText(tblId.getValueAt(fila, 3).toString());
            txtEmail.setText(tblId.getValueAt(fila, 4).toString());
            txtUsuario.setText(tblId.getValueAt(fila, 5).toString());
            txtPassword.setText(tblId.getValueAt(fila, 6).toString());
            txtConfirmar.setText(tblId.getValueAt(fila, 6).toString());
            cbTipoUsuario.setSelectedItem(tblId.getValueAt(fila, 7).toString());
            int activo = Integer.valueOf(tblId.getValueAt(fila, 8).toString());
            if (activo != 1) {
                cbxActivo.setSelected(false);
            } else {
                cbxActivo.setSelected(true);
            }
        } catch (Exception ex) {
        }
    }//GEN-LAST:event_tblIdMouseClicked

